// ============================================================
//  MenuPage.h
//  Базовий клас однієї "сторінки" режиму налаштування (розділ 4-А
//  специфікації). Кожна конкретна сторінка (список каналів, редагування
//  межі, ...) успадковує це і сама вирішує, як малюватись і як реагувати
//  на кнопку.
// ============================================================
#pragma once

#include <LiquidCrystal_I2C.h>
#include "MenuButton.h"

class MenuController;

class MenuPage {
public:
    virtual ~MenuPage() {}

    // Викликається щоразу при вході на сторінку (push або повернення з
    // дочірньої через pop) — тут скидати курсор/поле редагування тощо.
    virtual void onEnter() {}

    // Намалювати сторінку. MenuController кличе це і при вході, і після
    // кожного onButton() — тут не треба MonitorRow-подібного diff-кешу,
    // бо оновлення відбувається лише по натисканню кнопки, не 2р/сек.
    virtual void render(LiquidCrystal_I2C& lcd) = 0;

    // Обробити натискання. Сторінка сама мутує свій стан і/або просить
    // навігацію через nav.push()/nav.pop() — базовий клас про структуру
    // меню нічого не знає.
    virtual void onButton(MenuButton b, MenuController& nav) = 0;
};
